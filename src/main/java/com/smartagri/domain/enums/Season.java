package com.smartagri.domain.enums;

/**
 * Agricultural seasons that influence crop suitability and advisory rules.
 */
public enum Season {
    KHARIF,   // monsoon / summer crop (June – November)
    RABI,     // winter crop (October – March)
    ZAID,     // short-duration summer (March – June)
    PERENNIAL // year-round crops
}
